--Q7. Segment customers into New, Returning, and Loyal based on their total 
-- number of previous purchases, and show the count of each segment. 

SELECT
    CASE
        WHEN previous_purchases = 1  THEN 'New'
        WHEN previous_purchases <= 10 THEN 'Returning'
        WHEN previous_purchases >= 11 THEN 'Loyal'
    END AS customer_segment,
    COUNT(*) AS customer_segment_number
FROM customer
GROUP BY
    CASE
        WHEN previous_purchases = 1 THEN 'New'
        WHEN previous_purchases <= 10 THEN 'Returning'
        WHEN previous_purchases >= 11 THEN 'Loyal'
    END
ORDER BY customer_segment_number;



--Q8. What are the top 3 most purchased products within each category? 

SELECT item_purchased,category,COUNT(item_purchased) FROM customer
GROUP BY category,item_purchased
ORDER BY COUNT(item_purchased) DESC;

SELECT *
FROM (SELECT item_purchased,category,COUNT(item_purchased),
DENSE_RANK() OVER(PARTITION BY category ORDER BY COUNT(item_purchased) DESC) AS rank_category
FROM customer
GROUP BY category,item_purchased)
WHERE rank_category <= 3;

--Q9. Are customers who are repeat buyers (more than 5 previous purchases) also likely to subscribe?

SELECT customer_id,subscription_status,previous_purchases FROM customer
WHERE subscription_status = 'Yes' AND previous_purchases > 5 
ORDER BY previous_purchases DESC;

--Q10. What is the revenue contribution of each age group?

SELECT
    age,
    CASE
        WHEN age < 18 THEN 'Child'
        WHEN age BETWEEN 18 AND 30 THEN 'Young Adult'
        WHEN age BETWEEN 31 AND 50 THEN 'Adult'
        ELSE 'Senior'
    END AS age_group
FROM customer;


SELECT
CASE
