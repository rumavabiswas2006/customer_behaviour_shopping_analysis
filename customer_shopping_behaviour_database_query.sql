SELECT COUNT(*) FROM customer;


--Q1. What is the total revenue generated by male vs. female customers?

SELECT gender,SUM(purchase_amount_usd) AS revenue
FROM customer
GROUP BY gender
ORDER BY revenue;

--Q2. Which customers used a discount but still spent more than the average purchase amount? 

SELECT customer_id FROM customer
WHERE discount_applied = 'Yes' AND purchase_amount_usd >= (SELECT AVG(purchase_amount_usd) FROM customer);

-- Q3. Which are the top 5 products with the highest average review rating?

SELECT item_purchased,AVG(review_rating) FROM customer
GROUP BY item_purchased 
ORDER BY AVG(review_rating) DESC
LIMIT 5;

--Q4. Compare the average Purchase Amounts between Standard and Express Shipping

SELECT AVG(purchase_amount_usd),shipping_type FROM customer
WHERE shipping_type = 'Express' OR shipping_type = 'Standard'
GROUP BY shipping_type
ORDER BY AVG(purchase_amount_usd);

--Q5. Do subscribed customers spend more? Compare average spend and total revenue 

SELECT subscription_status,SUM(purchase_amount_usd) FROM customer
GROUP BY subscription_status
ORDER BY SUM(purchase_amount_usd);

--Q6. Which 5 products have the highest percentage of purchases with discounts applied?

SELECT
    item_purchased,
    ROUND(
        SUM(CASE WHEN discount_applied = 'Yes' THEN 1 END) * 100.0
        / COUNT(*),
        2
    ) AS discount_percentage
FROM customer
GROUP BY item_purchased
ORDER BY discount_percentage DESC
LIMIT 5;



